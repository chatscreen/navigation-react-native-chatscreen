class Category {
  constructor(
    id,
    title,
    image,
    address,
    visited,
    distance,
    type,
    color,
    about,
    price
  ) {
    this.id = id;
    this.title = title;
    this.image = image;
    this.address = address;
    this.visited = visited;
    this.distance = distance;
    this.type = type;
    this.color = color;
    this.about = about;
    this.price = price;
  }
}

export default Category;
