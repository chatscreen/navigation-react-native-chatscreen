class Category {
  constructor(id, title, image, address, visited, distance, type, color) {
    this.id = id;
    this.title = title;
    this.image = image;
    this.address = address;
    this.visited = visited;
    this.distance = distance;
    this.type = type;
    this.color = color;
  }
}

export default Category;
